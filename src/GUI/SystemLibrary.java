    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        jButton6.setEnabled(true);
        int del = JOptionPane.showConfirmDialog(this, "Are you sure to delete "
                +sysList.get(index).getSystem_code()+" in the system?", 
                "Confirmation Message", JOptionPane.YES_NO_OPTION);
        if(del == 0){
            try{
                lServ.deleteSystem(sysList.get(index));
                JOptionPane.showMessageDialog(this, "Processing system deleted!", 
                        "Information Message", JOptionPane.INFORMATION_MESSAGE);
                sysList = lServ.getSystem_all();
                m.displayProcessingSystem((DefaultTableModel)jTable1.getModel(), sysList);
                jButton6.doClick();
            }catch(ErrorException ex){
            }catch(FileNotFoundException ex){
            }catch(IOException ex){ }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

