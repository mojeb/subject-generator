    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String system_code = jTextField1.getText();
        String system_name = jTextField2.getText();
        
        if(system_code.isEmpty() || system_name.isEmpty()){
            JOptionPane.showMessageDialog(this, "Error! System Code or System name "
                    + "empty.", "Error Message", JOptionPane.ERROR_MESSAGE);
        }else{
            try{
                SystemClass sys = new SystemClass();
                sys.setSystem_code(system_code);
                sys.setSystem_name(system_name);
                
                //check duplicate
                if(m.checkSystemDuplicate(system_code) == 1 && isEdit == 0){
                    JOptionPane.showMessageDialog(this, "Error! System Code "+system_code+" "
                        + "already exists.", "Error Message", JOptionPane.ERROR_MESSAGE);
                }else{
                    if(isEdit == 0){
                        lServ.addSystem(sys);
                        JOptionPane.showMessageDialog(this, "Successfully added new processing system.", 
                                "Information Message", JOptionPane.INFORMATION_MESSAGE);
                    }else{
                        sys.setSystem_id(sysList.get(index).getSystem_id());
                        lServ.updateSystem(sys);
                        JOptionPane.showMessageDialog(this, "Successfully updated processing system.", 
                                "Information Message", JOptionPane.INFORMATION_MESSAGE);
                    }
                    jButton6.doClick();
                    //display
                    sysList = lServ.getSystem_all();
                    m.displayProcessingSystem((DefaultTableModel)jTable1.getModel(), sysList);
                }
                
            }catch(ErrorException ex){
            }catch(FileNotFoundException ex){
            }catch(IOException ex){ }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

