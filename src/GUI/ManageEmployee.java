    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String emp_id = jFormattedTextField1.getText();
        String lname = jTextField1.getText();
        String fname = jTextField2.getText();
        String mname = jTextField3.getText();
        String sname = jTextField4.getText();
        String position = jTextField5.getText();
        String dept = jComboBox1.getSelectedItem().toString();
        
        if(emp_id.isEmpty() || lname.isEmpty() || fname.isEmpty() || mname.isEmpty()
                || position.isEmpty() || dept.compareToIgnoreCase("Select Department") == 0){
            JOptionPane.showMessageDialog(this, "Error! Please complete the required information!", 
                    "Error Message", JOptionPane.ERROR_MESSAGE);
        }else{
            try{
                Department d = dServ.getDepartmentbyCode(dept);
                
                Person p = new Person();
                if(isEdit == -1)
                    p.setEmp_id(emp_id);
                p.setEmp_lname(lname);
                p.setEmp_fname(fname);
                p.setEmp_mname(mname);
                p.setEmp_sname(sname);
                p.setEmp_position(position);
                p.setEmp_department(d);
                //save data into the database
                if(isEdit == -1){//new employee
                    pServ.addPerson(p);
                    JOptionPane.showMessageDialog(this, emp_id+" has been successfully saved!", 
                            "Information Message", JOptionPane.INFORMATION_MESSAGE);
                }else{//update employee
                    p.setEmp_id(person.getEmp_id());
                    pServ.updatePerson(p);
                    JOptionPane.showMessageDialog(this, person.getEmp_id()+" has been successfully updated!", 
                            "Information Message", JOptionPane.INFORMATION_MESSAGE);
                }
                jButton5.doClick();
            }catch(ErrorException ex){ }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

