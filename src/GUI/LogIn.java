    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String username = jTextField1.getText();
        String password = new String(jPasswordField1.getPassword());
        if(username.isEmpty() || password.isEmpty()){
            JOptionPane.showMessageDialog(this, "Error! Username and password required.", 
                    "Error Message", JOptionPane.ERROR_MESSAGE);
        }else{
            try{
                Account acc = aServ.getAccount_emp_id(username);
                if(acc == null){
                    JOptionPane.showMessageDialog(this, "Error! Username "+username+" doesn't exists.", 
                        "Error Message", JOptionPane.ERROR_MESSAGE);
                }else{
                    if(acc.getPassword().equals(password)){
                        this.logIn.setEnabled(false);
                        this.logOut.setEnabled(true);
                        this.pass.setEnabled(true);
                        rList = aServ.getRight_emp_id(acc.getEmp().getEmp_id());
                        examine_rights();
                        Main_Window.account = acc;
                        this.dispose();
                    }else{
                        JOptionPane.showMessageDialog(this, "Error! Incorrect password.", 
                                    "Error Message", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }catch(ErrorException ex){
            }catch(FileNotFoundException ex){
            }catch(IOException ex){}
        }
    }//GEN-LAST:event_jButton1ActionPerformed

