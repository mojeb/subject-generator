    private void examine_rights(){
        if(rList != null){
            ArrayList<String> rlist = new ArrayList<String>();
            for(Right r : rList){
                if(r.getStatus().equals("Active"))
                    rlist.add(r.getRight());
            }
            
            if(rlist.contains("HRU Personnel")){
                this.hr_menu1.setEnabled(true);
                this.hr_menu2.setEnabled(true);
                this.hr_menu3.setEnabled(true);
                this.hr_menu4.setEnabled(true);
            }
            
            if(rlist.contains("Administrator")){
                this.sys_lib.setEnabled(true);
                this.user_lib.setEnabled(true);
                this.manage_emp.setEnabled(true);
                this.manage_accnt.setEnabled(true);
                this.manage_office.setEnabled(true);
                this.database.setEnabled(true);
            }
            
            if(rlist.contains("ORVP Personnel")){
                this.so.setEnabled(true);
                this.memo.setEnabled(true);
                this.oo.setEnabled(true);
            }
            
            if(rlist.contains("GSU Personnel")){
                this.riv.setEnabled(true);
                this.po.setEnabled(true);
                this.list.setEnabled(true);
                this.print.setEnabled(true);
            }
        }
    }
