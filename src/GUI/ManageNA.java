    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String emp_id = jTextField3.getText();
        String control_no = jTextField4.getText();
        String remarks = jTextArea1.getText();
        
        if(emp_id.isEmpty() || control_no.isEmpty() || dList.isEmpty()){
            JOptionPane.showMessageDialog(this, "Error! Please complete information needed.", 
                    "Error Message", JOptionPane.ERROR_MESSAGE);
        }else{
            if(jRadioButton4.isSelected()){
                nature = jTextField1.getText();
            }
            
            if(nature.isEmpty()){
                JOptionPane.showMessageDialog(this, "Error! Nature of absence not specified.", 
                    "Error Message", JOptionPane.ERROR_MESSAGE);
            }else{
                try{
                    Person p = pServ.getPersonID(emp_id);
                    if(p == null){
                        JOptionPane.showMessageDialog(this, "Error! Employee with ID No. "+emp_id+" doesn't exists.", 
                                "Error Message", JOptionPane.ERROR_MESSAGE);
                    }else{
                        NA na = new NA();
                        na.setControl_no(control_no);
                        na.setEmp(p);
                        na.setNature(nature);
                        na.setRemarks(remarks);

                        if(jLabel3.getText().equalsIgnoreCase("New Notice of Absence")){
                            //meaning new
                            na.setYear(m.getCurrentYear());
                            na.setSeq(naServ.getLastSeq(m.getCurrentYear())+1);
                            
                            naServ.addNA(na);
                            //save DaysAbsent
                            for(DaysAbsent d : dList){
                                d.setControl_no(control_no);
                                naServ.addDaysAbsent(d);
                            }
                            
                            ConfirmationMessage conf = new ConfirmationMessage(this,
                                    true, "New Notice of Absence", control_no);
                            conf.setVisible(true);
                        }else if(jLabel3.getText().equalsIgnoreCase("Update Notice of Absence")){
                            //update
                            naServ.updateNA(na);
                            //delete muna all days of absent before saving again
                            DaysAbsent d = new DaysAbsent();
                            d.setControl_no(control_no);
                            naServ.deleteDaysAbsent(d);
                            //you can now save the new list
                            for(DaysAbsent day : dList){
                                day.setControl_no(control_no);
                                naServ.addDaysAbsent(day);
                            }
                            
                            ConfirmationMessage conf = new ConfirmationMessage(this,
                                    true, "Notice of Absence Updated", control_no);
                            conf.setVisible(true);
                        }
                    }
                }catch(ErrorException ex){
                }catch(FileNotFoundException ex){
                }catch(IOException ex){}
                
                jButton6.setEnabled(true);
                jButton6.doClick();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

