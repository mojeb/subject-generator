    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        jLabel3.setText("Delete Notice of Absence");
        String control_no = jTextField4.getText();
        int del = JOptionPane.showConfirmDialog(this, "Are you sure to delete Notice of Absence "+
                control_no+" ?", "Confirmation Message", JOptionPane.YES_NO_OPTION);
        
        if(del == 0){
            try{
                //delete NA
                NA na = new NA();
                na.setControl_no(control_no);
                naServ.deleteNA(na);
                //delete days absent
                DaysAbsent d = new DaysAbsent();
                d.setControl_no(control_no);
                naServ.deleteDaysAbsent(d);
                
                ConfirmationMessage conf = new ConfirmationMessage(this,
                        true, "Notice of Absence Deleted", control_no);
                conf.setVisible(true);
            }catch(ErrorException ex){
            }catch(FileNotFoundException ex){
            }catch(IOException ex){}
            jButton6.setEnabled(true);
            jButton6.doClick();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

