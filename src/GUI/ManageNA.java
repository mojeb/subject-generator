    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        Date date1 = null;
        Date date2 = null;
        String t = jComboBox1.getSelectedItem().toString();
        if(t.equalsIgnoreCase("Single Date")){
            date1 = jDateChooser1.getDate();
            if(date1 == null){
                JOptionPane.showMessageDialog(this, "Error! Date input required.", 
                        "Error Message", JOptionPane.ERROR_MESSAGE);
            }else{
                DaysAbsent d = new DaysAbsent();
                d.setStr_date((date1.getMonth()+1)+"/"+date1.getDate()+"/"+
                        (date1.getYear()+1900));
                dList.add(d);
                //display date
                m.displayDaysAbsent(jList1, dList);
            }
        }else{
            date1 = jDateChooser1.getDate();
            date2 = jDateChooser2.getDate();
            if(date1 == null || date2 == null){
                JOptionPane.showMessageDialog(this, "Error! Date inputs are required.", 
                        "Error Message", JOptionPane.ERROR_MESSAGE);
            }else{
                DaysAbsent d = new DaysAbsent();
                d.setStr_date((date1.getMonth()+1)+"/"+date1.getDate()+"/"+
                        (date1.getYear()+1900)+" - "+(date2.getMonth()+1)+"/"+
                        date2.getDate()+"/"+(date2.getYear()+1900));
                dList.add(d);
                //display date
                m.displayDaysAbsent(jList1, dList);
            }
        }
        
        jDateChooser1.setDate(null);
        jDateChooser2.setDate(null);
    }//GEN-LAST:event_jButton8ActionPerformed

