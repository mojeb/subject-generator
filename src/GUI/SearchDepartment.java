    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String code = jTextField1.getText();
        String name = jTextArea1.getText();
        if(!jRadioButton1.isSelected() && !jRadioButton2.isSelected() && !jRadioButton3.isSelected()){
            JOptionPane.showMessageDialog(this, "Error! Please select search option!", 
                    "Error Message", JOptionPane.ERROR_MESSAGE);
        }else{
            try{
                if(jRadioButton1.isSelected()){//search by code
                    if(code.isEmpty()){
                        JOptionPane.showMessageDialog(this, "Error! Please indicate department code!", 
                        "Error Message", JOptionPane.ERROR_MESSAGE);
                    }else{
                        dList = dServ.getDepartmentCode(code);
                        //display data
                        m.displayDepartment(table, dList);
                        this.dispose();
                    }
                }else if(jRadioButton2.isSelected()){//search by name
                    if(name.isEmpty()){
                        JOptionPane.showMessageDialog(this, "Error! Please indicate department name!", 
                        "Error Message", JOptionPane.ERROR_MESSAGE);
                    }else{
                        dList = dServ.getDepartmentName(name);
                        //display data
                        m.displayDepartment(table, dList);
                        this.dispose();
                    }
                }else if(jRadioButton3.isSelected()){//search all
                    dList = dServ.getAllDepartment();
                    //display data
                    m.displayDepartment(table, dList);
                    this.dispose();
                }
            }catch(ErrorException ex){}
            ManageDepartment.dList = dList;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

