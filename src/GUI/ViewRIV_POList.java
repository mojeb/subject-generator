    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            String year = jComboBox2.getSelectedItem().toString();
            String month = jComboBox1.getSelectedItem().toString();
            if(year.equals("Year") || month.equals("Select Month")){
                JOptionPane.showMessageDialog(this, "Error! Year and month properly.", 
                        "Error Message", JOptionPane.ERROR_MESSAGE);
            }else{
                Date date = new Date(Integer.parseInt(year), jComboBox1.getSelectedIndex()+1, 1);
                rivList.clear();
                rivList = rivServ.getRIV_month(date, Integer.parseInt(year));
            }
            //display rivList
            m.displayRIV((DefaultTableModel) jTable1.getModel(), rivList);
        }catch(ErrorException ex){}
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String month = jComboBox3.getSelectedItem().toString();
        String year = jComboBox4.getSelectedItem().toString();
        
        if(month.equals("Select Month") || year.equals("Year")){
            JOptionPane.showMessageDialog(this, "Error! Please select month and year properly.", 
                    "Error Message", JOptionPane.ERROR_MESSAGE);
        }else{
            try{
                Date date = new Date(Integer.parseInt(year), jComboBox3.getSelectedIndex()+1, 1);
                poList.clear();
                poList = poServ.getPO_month(date, Integer.parseInt(year));
            }catch(ErrorException ex){}
            //display poList...
            m.displayPO((DefaultTableModel) jTable2.getModel(), poList);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

