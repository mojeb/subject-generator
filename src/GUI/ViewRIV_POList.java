    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        if(poList.isEmpty()){
            JOptionPane.showMessageDialog(this, "No data to print.", 
                    "Information Message", JOptionPane.INFORMATION_MESSAGE);
        }else{
            PrintData print = new PrintData();
            print.setPOList(poList);
            print.setStringDate(jComboBox3.getSelectedItem().toString()+" "+
                    jComboBox4.getSelectedItem().toString());
            PrintPreview pv = new PrintPreview(this, true, print);
            pv.setVisible(true);
            if(pv.isPrint == 1)
                print.showPrintDialog();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

