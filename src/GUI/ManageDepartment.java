    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String code = jTextField1.getText();
        String name = jTextArea1.getText();
        
        if(code.isEmpty() || name.isEmpty()){
            JOptionPane.showMessageDialog(this, "Error! Please indicate the Office Code and Office Name!", 
                    "Error Message", JOptionPane.ERROR_MESSAGE);
        }else{
            try{
                Department d = new Department();
                d.setDept_code(code);
                d.setDept_name(name);
                d.setPerson(new Person());
                if(isEdit == 1)
                    d.setDept_id(dept.getDept_id());
                if(person != null)
                    d.setPerson(person);
                
                if(isEdit == -1){//new department
                    //check for duplicate
                    if(m.checkDepartment(d) == 0){
                        //add new department
                        dServ.addDepartment(d);
                        JOptionPane.showMessageDialog(this, "Department Saved!", 
                                "Information Message", JOptionPane.INFORMATION_MESSAGE);
                        //display
                        dList.add(d);
                        m.displayDepartment((DefaultTableModel)jTable1.getModel(), dList);
                    }else{
                        JOptionPane.showMessageDialog(this, "Error! Duplicate found. "
                                + "Either department code or department name already exists!", 
                                "Error Message", JOptionPane.ERROR_MESSAGE);
                    }
                }else{//edit department
                    System.out.print(d.getDept_code());
                    dServ.updateDepartment(d);
                    JOptionPane.showMessageDialog(this, "Department Updated!", 
                            "Information Message", JOptionPane.INFORMATION_MESSAGE);
                    isEdit = -1;
                    dList.remove(index);
                    dList.add(d);
                    m.displayDepartment((DefaultTableModel)jTable1.getModel(), dList);
                }
                //clear fields
                jButton9.setEnabled(false);
                jButton5.doClick();
                jButton1.setEnabled(true);
            }catch(ErrorException ex){ }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

