    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        jButton5.doClick();
        jButton9.setEnabled(false);
        index = jTable1.getSelectedRow();
        if(index >= 0){
            jButton3.setEnabled(true);
            jButton8.setEnabled(true);
            //display info
            dept = dList.get(index);
            jTextField1.setText(dept.getDept_code());
            jTextArea1.setText(dept.getDept_name());
            //display head if any
            Person p = new Person();
            try{
                if(!dept.getPerson().getEmp_id().isEmpty()){
                    p = pServ.getPersonID(dept.getPerson().getEmp_id());
                    jLabel7.setText(p.getEmp_id()+"");
                    jLabel8.setText(p.getCompleteName());
                    jLabel9.setText(p.getEmp_position());
                }else{
                    jLabel7.setText("");
                    jLabel8.setText("");
                    jLabel9.setText("");
                }
            }catch(ErrorException ex){}
        }
    }//GEN-LAST:event_jTable1MouseClicked

