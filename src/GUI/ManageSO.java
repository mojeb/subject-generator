    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if(subject.equals("Manage Special Order"))
            jLabel3.setText("Delete Special Order");
        else if(subject.equals("Manage Office Order"))
            jLabel3.setText("Delete Office Order");
        else if(subject.equals("Manage Memorandum"))
            jLabel3.setText("Delete Memorandum");
        
        String so_no = jLabel7.getText();
        
        int del = JOptionPane.showConfirmDialog(this, "Are you sure to delete SO No. "+
                so_no+" ?", "Confirmation Message", JOptionPane.YES_NO_OPTION);
        
        if(del == 0){
            try{
                SO so = soServ.getSO_no_subject(so_no, subject);
                soServ.deleteSO(so);
                
                JOptionPane.showMessageDialog(this, "Special Order No. "+so_no+
                        " successfully deleted.", "Information Message", 
                        JOptionPane.INFORMATION_MESSAGE);
            }catch(ErrorException ex){ }
            catch(FileNotFoundException ex){ }
            catch(IOException ex){ }
            
            jButton6.setEnabled(true);
            jButton6.doClick();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

