    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String so_no = jLabel7.getText();
        String des = jTextArea1.getText();
        
        if(des.isEmpty()){
            JOptionPane.showMessageDialog(this, "Error! Special Order description/subject required.", 
                    "Error Message", JOptionPane.ERROR_MESSAGE);
        }else{
            try{
                SO so = new SO();
                so.setSo_no(so_no);
                so.setDescription(des);
                so.setSubject(subject);
                
                String process = jLabel3.getText();

                if(process.equalsIgnoreCase("New Special Order") || process.equalsIgnoreCase("New Office Order")
                        || process.equalsIgnoreCase("New Memorandum")){
                    so.setYear(m.getCurrentYear());
                    so.setSeq(soServ.getLastSeq(m.getCurrentYear(), subject)+1);
                    
                    soServ.addSO(so);
                    ConfirmationMessage conf = new ConfirmationMessage(this,
                            true, "New ORVP Order Created", so_no);
                    conf.setVisible(true);
                }else if(process.equalsIgnoreCase("Update Special Order") || process.equalsIgnoreCase("Update Office Order")
                        || process.equalsIgnoreCase("Update Memorandum")){
                    soServ.editSO(so);
                    ConfirmationMessage conf = new ConfirmationMessage(this,
                            true, "ORVP Order Updated", so_no);
                    conf.setVisible(true);
                }
            }catch(ErrorException ex){ }
            catch(FileNotFoundException ex){ }
            catch(IOException ex){ }
            
            jButton6.setEnabled(true);
            jButton6.doClick();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

