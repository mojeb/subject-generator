    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!jCheckBox1.isSelected() && !jCheckBox2.isSelected()){
            JOptionPane.showMessageDialog(this, "Error! Please select user account type.", 
                    "Error Message", JOptionPane.ERROR_MESSAGE);
        }else{
            String system = jComboBox1.getSelectedItem().toString();
            if(system.equalsIgnoreCase("Select Processing System")){
                JOptionPane.showMessageDialog(this, "Error! Please select processing system.", 
                    "Error Message", JOptionPane.ERROR_MESSAGE);
            }else{
                String type = "";
                if(jCheckBox1.isSelected() && jCheckBox2.isSelected()){
                    type = "system account and terminal account";
                }else if(!jCheckBox1.isSelected() && jCheckBox2.isSelected()){
                    type = "system account";
                }else if(jCheckBox1.isSelected() && !jCheckBox2.isSelected()){
                    type = "terminal account";
                }
                
                if(process == 1)
                    text = "Request for the creation of "+system+" "+type;
                else
                    text = "Request for password reset of "+system+" "+type;
                    
                a1.setText("");
                a1.setText(text);
                this.dispose();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

