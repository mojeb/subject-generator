    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int ndex = jTable1.getSelectedRow();
        RIV r = rivList.get(ndex);
        if(iden == 1){
            l1.setText(r.getControl_no());
            t1.setText(r.getDate().toString());
            t2.setText(r.getEmp().getEmp_id());
            t3.setText(r.getEmp().getCompleteName());
            t4.setText(r.getEmp().getEmp_position());
            t5.setText(r.getEmp().getEmp_department().getDept_code());
            a1.setText(r.getDescription());
            a2.setText(r.getRemarks());
            b1.setEnabled(true);
            b2.setEnabled(true);
            b3.setEnabled(true);
        }else if(iden == 2){
            ManagePO.riv = r;
            t1.setText(r.getControl_no());
            a1.setText(r.getDescription());
        }
        this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

