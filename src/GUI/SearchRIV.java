    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            if(jRadioButton1.isSelected() || jRadioButton2.isSelected()){
                String search_item = jTextField1.getText();
                if(search_item.isEmpty()){
                    JOptionPane.showMessageDialog(this, "Error! RIV No. or Employee ID required.", 
                            "Error Message", JOptionPane.ERROR_MESSAGE);
                }else{
                    if(jRadioButton1.isSelected()){//search riv
                        RIV riv = rivServ.getRIV_control_no(search_item);
                        if(riv == null){
                            JOptionPane.showMessageDialog(this, "Error! RIV No. "+search_item+" doesn't exists.", 
                                    "Error Message", JOptionPane.ERROR_MESSAGE);
                        }else{
                            rivList.clear();
                            rivList.add(riv);
                        }
                    }else if(jRadioButton2.isSelected()){//search employee
                        Person p = pServ.getPersonID(search_item);
                        if(p == null){
                            JOptionPane.showMessageDialog(this, "Error! Employee ID "+search_item+" doesn't exits.", 
                                    "Error Message", JOptionPane.ERROR_MESSAGE);
                        }else{
                            rivList.clear();
                            rivList = rivServ.getRIV_emp_id(search_item);
                        }
                    }
                }
            }else if(jRadioButton3.isSelected()){
                String year = jComboBox2.getSelectedItem().toString();
                String month = jComboBox1.getSelectedItem().toString();
                if(year.equals("Year") || month.equals("Select Month")){
                    JOptionPane.showMessageDialog(this, "Error! Year and month properly.", 
                            "Error Message", JOptionPane.ERROR_MESSAGE);
                }else{
                    Date date = new Date(Integer.parseInt(year), jComboBox1.getSelectedIndex()+1, 1);
                    rivList.clear();
                    rivList = rivServ.getRIV_month(date, Integer.parseInt(year));
                }
            }
            
            //display rivList
            m.displayRIV((DefaultTableModel) jTable1.getModel(), rivList);
        }catch(ErrorException ex){}
    }//GEN-LAST:event_jButton1ActionPerformed

