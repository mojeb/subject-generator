    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:
        Date date = jDateChooser1.getDate();
        if(date == null){
            JOptionPane.showMessageDialog(this, "Error! Please specify date to be corrected.", 
                    "Error Message", JOptionPane.ERROR_MESSAGE);
        }else{
            String in_am = jTextField4.getText();
            String out_am = jTextField5.getText();
            String in_pm = jTextField6.getText();
            String out_pm = jTextField7.getText();
            
            if(in_am.isEmpty() && out_am.isEmpty() && in_pm.isEmpty() &&
                    out_pm.isEmpty()){
                JOptionPane.showMessageDialog(this, "Error! All fields cannot be empty.", 
                        "Error Message", JOptionPane.ERROR_MESSAGE);
            }else{
                DateCorrected dc = new DateCorrected();
                dc.setDate(date);
                dc.setIn_am(in_am);
                dc.setOut_am(out_am);
                dc.setIn_pm(in_pm);
                dc.setOut_pm(out_pm);
                
                if(isEdit == 1)
                    dateCorrList.remove(index);
                dateCorrList.add(dc);
                //display in table
                m.displayDateCorrected((DefaultTableModel)jTable1.getModel(), dateCorrList);
                jButton14.doClick();
            }
        }
    }//GEN-LAST:event_jButton11ActionPerformed

