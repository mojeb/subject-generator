    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        jLabel3.setText("Delete Daily Time Correction");
        String control_no = jTextField3.getText();
        int del = JOptionPane.showConfirmDialog(this, "Are you sure to delete DTC with"
                + " Control No.: "+control_no+" ?", "Confirmation Message", 
                JOptionPane.YES_NO_OPTION);
        if(del == 0){
            try{
                //delete dtc in database
                DTC dtc = new DTC();
                dtc.setControl_no(control_no);
                dtServ.deleteDTC(dtc);
                //remove date corrected
                DateCorrected dateCorr = new DateCorrected();
                dateCorr.setControl_no(control_no);
                dtServ.deleteDateCorrected(dateCorr);
                
                JOptionPane.showMessageDialog(this, "Daily Time Correction deleted with "
                        + "Control No.: "+control_no+".", "Information Message", 
                        JOptionPane.INFORMATION_MESSAGE);
            }catch(ErrorException ex){ }
            catch(FileNotFoundException ex){ }
            catch(IOException ex){ }
        }
        jButton6.setEnabled(true);
        jButton6.doClick();
    }//GEN-LAST:event_jButton4ActionPerformed

