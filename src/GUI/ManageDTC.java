    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String emp_id = jTextField2.getText();
        String reason = jTextArea1.getText();
        String control_no = jTextField3.getText();
        String remarks = jTextArea2.getText();
        
        if(emp_id.isEmpty() || reason.isEmpty() || control_no.isEmpty() || 
                dateCorrList.isEmpty()){
            JOptionPane.showMessageDialog(this, "Error! Field(s) empty!", 
                    "Error Message", JOptionPane.ERROR_MESSAGE);
        }else{
            try{
                Person p = new Person();
                p = pServ.getPersonID(emp_id);
                
                if(p == null){
                    JOptionPane.showMessageDialog(this, "Error! Employee with ID No. "+
                            emp_id+" not existing.", "Error Message", 
                            JOptionPane.ERROR_MESSAGE);
                }else{
                    DTC dtc = new DTC();
                    dtc.setControl_no(control_no);
                    dtc.setEmployee(p);
                    dtc.setReason(reason);
                    dtc.setRemarks(remarks);

                    if(jLabel3.getText().equalsIgnoreCase("New Daily Time Correction")){
                        //new DTC
                        dtc.setYear(m.getCurrentYear());
                        dtc.setSeq(dtServ.getLastSeq(m.getCurrentYear())+1);

                        dtServ.addDTC(dtc);
                        //save dateCorrList
                        for(int i=0; i<dateCorrList.size(); i++){
                            DateCorrected dateCorr = dateCorrList.get(i);
                            dateCorr.setControl_no(control_no);
                            dtServ.addDateCorrected(dateCorr);
                        }
                        dateCorrList.clear();//remove all elements in the list

                        ConfirmationMessage conf = new ConfirmationMessage(this,
                                true, "New Daily Time Correction", control_no);
                        conf.setVisible(true);
                    }else if(jLabel3.getText().equalsIgnoreCase("Update Daily Time Correction")){
                        //update DTC
                        dtServ.updateDTC(dtc);
                        //remove all date corrected befor saving the new
                        DateCorrected dateCorr = new DateCorrected();
                        dateCorr.setControl_no(control_no);
                        dtServ.deleteDateCorrected(dateCorr);
                        //save the new date corrected
                        for(int i=0; i<dateCorrList.size(); i++){
                            dateCorr = dateCorrList.get(i);
                            dateCorr.setControl_no(control_no);
                            dtServ.addDateCorrected(dateCorr);
                        }
                        dateCorrList.clear();//remove all elements in the list
                        
                        ConfirmationMessage conf = new ConfirmationMessage(this,
                                true, "Daily Time Correction Updated", control_no);
                        conf.setVisible(true);
                    }
                    jButton6.doClick();
                }
            }catch(ErrorException ex){ }
            catch(FileNotFoundException ex){ }
            catch(IOException ex){ }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

