    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String distination = jTextField2.getText();
        Date date_travel = jDateChooser1.getDate();
        String control_no = jTextField4.getText();
        String remarks = jTextArea1.getText();
        
        if(empList.isEmpty() || distination.isEmpty() || date_travel == null
                || control_no.isEmpty()){
            JOptionPane.showMessageDialog(this, "Error! Please complete the "
                    + "required information! ", "Error Message", 
                    JOptionPane.ERROR_MESSAGE);
        }else{
            try{
                OBS obs_var = new OBS();
                obs_var.setControl_no(control_no);
                obs_var.setDistination(distination);
                obs_var.setDate_travel(date_travel);
                obs_var.setRemarks(remarks);

                if(jLabel3.getText().equalsIgnoreCase("New Official Business Slip")){
                    //new OBS
                    obs_var.setYear(m.getCurrentYear());
                    obs_var.setSeq(oServ.getLastSeq(m.getCurrentYear())+1);
                    oServ.addOBS(obs_var);
                    
                    //save employee
                    for(OBS_employee emp : empList){
                        emp.setControl_no(control_no);
                        oServ.addOBS_employee(emp);
                    }
                    ConfirmationMessage conf = new ConfirmationMessage(this,
                            true, "New Official Business Slip", control_no);
                    conf.setVisible(true);
                }else if(jLabel3.getText().equalsIgnoreCase("Update Official Business Slip")){
                    //update OBS
                    oServ.updateOBS(obs_var);
                    
                    //delete muna
                    OBS_employee xxx = new OBS_employee();
                    xxx.setControl_no(control_no);
                    oServ.deleteOBS_employee(xxx);
                    //save the new
                    for(OBS_employee emp : empList){
                        emp.setControl_no(control_no);
                        oServ.addOBS_employee(emp);
                    }
                    ConfirmationMessage conf = new ConfirmationMessage(this,
                            true, "Official Business Slip Updated", control_no);
                    conf.setVisible(true);
                }
                jButton5.setEnabled(true);
                jButton5.doClick();
                    
            }catch(ErrorException ex){ }
            catch(FileNotFoundException ex){ }
            catch(IOException ex){ }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

