    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // TODO add your handling code here:
        String emp_id = jTextField1.getText();
        if(empList.size() < 5){
            try{
                Person p = pServ.getPersonID(emp_id);
                if(p == null){
                    JOptionPane.showMessageDialog(this, "Error! Employee ID No. "+emp_id+" doesn't exists.", 
                                "Error Message", JOptionPane.ERROR_MESSAGE);
                }else{
                    //check if employee alraedy in the list
                    if(m.check_employee_id(empList, emp_id) == 1){
                        JOptionPane.showMessageDialog(this, "Error! Employee ID No. "+emp_id+" already in the list.", 
                                "Error Message", JOptionPane.ERROR_MESSAGE);
                    }else{
                        OBS_employee emp = new OBS_employee();
                        emp.setP(p);
                        empList.add(emp);
                        m.displayEmp_OBS(jList1, empList);
                        jTextField1.setText("");
                    }
                }
            }catch(ErrorException ex){}
        }else{
            JOptionPane.showMessageDialog(this, "Error! Reached the maximum employee allowed.", 
                    "Error Message", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton10ActionPerformed

