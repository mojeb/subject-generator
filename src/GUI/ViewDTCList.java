    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        if (index >= 0) {
            try{
                DTC dtc = dtcList.get(index);
                //display date corrected
                dateCorrList = dtServ.getDateCorrected_control_no(dtc.getControl_no());
                m.displayDateCorrected((DefaultTableModel)jTable2.getModel(), dateCorrList);
            }catch(ErrorException ex){ }
            catch(FileNotFoundException ex){ }
            catch(IOException ex){ }
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String year = jComboBox2.getSelectedItem().toString();
        String month = jComboBox1.getSelectedItem().toString();
        
        if(year.equalsIgnoreCase("Year") || month.equalsIgnoreCase("Select Month")){
            JOptionPane.showMessageDialog(this, "Error! Please select month and "
                    + "year properly.", "Error Message", 
                    JOptionPane.ERROR_MESSAGE);
            m.clearTable((DefaultTableModel)jTable1.getModel());
            m.clearTable((DefaultTableModel)jTable2.getModel());
        }else{
            String date1 = year+"-"+m.convert_month(month)+"-"+1;
            String date2 = year+"-"+m.convert_month(month)+"-"+m.get_last_day_month(month);

            try{
                dtcList = dtServ.getDTC_date_app(date1, date2);
                m.displayDTC((DefaultTableModel)jTable1.getModel(), dtcList);
            }catch(ErrorException ex){ }
            catch(FileNotFoundException ex){ }
            catch(IOException ex){ }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

