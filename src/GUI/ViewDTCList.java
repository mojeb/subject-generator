    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(dtcList.isEmpty()){
            JOptionPane.showMessageDialog(this, "No data to print.", 
                    "Information Message", JOptionPane.INFORMATION_MESSAGE);
        }else{
            PrintData print = new PrintData();
            print.setDTCList(dtcList);
            print.setStringDate(jComboBox1.getSelectedItem().toString()+" "+
                    jComboBox2.getSelectedItem().toString());
            PrintPreview pv = new PrintPreview(this, true, print);
            pv.setVisible(true);
            if(pv.isPrint == 1)
                print.showPrintDialog();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

