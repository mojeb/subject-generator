    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String emp_id = jTextField1.getText();
        String control_no = jTextField2.getText();
        
        if(emp_id.isEmpty() && control_no.isEmpty()){
            JOptionPane.showMessageDialog(this, "Error! No object to search.", 
                    "Error Message", JOptionPane.ERROR_MESSAGE);
        }else{
            try{
                dtcList.clear();
                if(!emp_id.isEmpty()){
                    dtcList = dtServ.getDTC_employee(emp_id);
                }else if(!control_no.isEmpty()){
                    DTC dtc = dtServ.getDTC_control_no(control_no);
                    dtcList.add(dtc);
                }
            }catch(ErrorException ex){ }
            catch(FileNotFoundException ex){ }
            catch(IOException ex){ }
            //display data...
            jButton3.setEnabled(false);
            m.displayDTC((DefaultTableModel)jTable1.getModel(), dtcList);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

