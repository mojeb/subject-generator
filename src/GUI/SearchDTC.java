    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(iden == 1){
            DTC dtc = dtcList.get(index);
//            t1.setText(dtc.getDate_app().getMonth()+"/"+dtc.getDate_app().getDate()+
//                    "/"+(dtc.getDate_app().getYear()+1900));
            t1.setText(dtc.getDate_app().toString());
            t2.setText(dtc.getEmployee().getEmp_id());
            a1.setText(dtc.getReason());
            t3.setText(dtc.getControl_no());
            a2.setText(dtc.getRemarks());
            l1.setText(dtc.getControl_no());
            //query the corrected date
            ArrayList<DateCorrected> dateCorrList;
            try{
                dateCorrList = dtServ.getDateCorrected_control_no(dtc.getControl_no());
                ManageDTC.dateCorrList = dateCorrList;
            }catch(ErrorException ex){ }
            catch(FileNotFoundException ex){ }
            catch(IOException ex){ }
            b1.setEnabled(true);
            b2.setEnabled(true);
        }
        this.dispose();
    }//GEN-LAST:event_jButton3ActionPerformed

